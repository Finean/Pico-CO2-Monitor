def character(buffer, text, xt, yt, size, colour):  # Single character sz is size: 1 or 2
    if 32 <= text <= 126:
        pass
    else:
        text = ord(" ")
    code = text * 5    # 5 bytes per character
    for ii in range(5):
        line = FONT[code + ii]
        for yy in range(8):
            if (line >> yy) & 0x1:
                #Do not draw outside of frame buffer
                xv = ii * size + xt
                yv = yy * size + yt
                if xv > WIDTH or yv > HEIGHT:
                    continue
                
                if size > 1:
                    buffer.rect(xv, yv, min(size, WIDTH - xv), min(size, HEIGHT - yv), colour, True)
                else:
                    buffer.pixel(xv, yv, colour)
   
   
def text(buffer, text, x, y, size, colour): #Draws text on one line, starting at x, y
    space = (size + 1) // 2
    char_width = 5 * size + space
    for letter in text:
        char = ord(letter)
        character(buffer, char, x, y, size, colour)
        x += char_width
        
        
def measure(text, size):
    space = (size + 1) // 2
    char_width = 5 * size + space
    return(char_width * len(text) - space)

#Draw text, aligned either left or right, use ... for centered text
def textbox(buffer, text, x, y, box_width, text_colour, box_colour, text_size = 3, align = "left", offset = (0,0), draw = True):
    box_width = min(box_width, WIDTH - x)
    space = (text_size + 1) // 2
    char_width = 5 * text_size + space
    text_len = len(text)
    lines = 2
    chars_per_line = (box_width - 2 * offset[0]) // char_width
    lines = (text_len // chars_per_line) if (text_len % chars_per_line == 0) else (1 + text_len // chars_per_line)
    height = (lines * 8 * text_size) + (2 * offset[1]) + (lines - 1) * text_size
    
    #Draw Box
    if draw:
        if not(box_colour is None):
            buffer.rect(x, y, box_width, height, box_colour, True)
        if align == "left":
            r_bound = min(box_width - offset[0], WIDTH)
            xt = x + offset[1]
            yt = y + offset[1]
            for char in text:
                if xt + text_size * 5 > r_bound:
                    xt = x + offset[1]
                    yt += 8 * text_size + text_size#Second one is the size of the line-break
                    if char == " " and align == "left":
                        continue
                code = ord(char)
                character(buffer, code, xt, yt, text_size, text_colour)
                xt += char_width       
    return(height)


def wordbox(buffer, message, x, y, box_width, text_colour, box_colour, text_size = 3, align = "center", offset = (0,0), draw = True):
    box_width = min(box_width, WIDTH - x)
    space = (text_size + 1) // 2
    char_width = 5 * text_size + space
    line_break = text_size
    long_word = 12 #Words at least this long may be split
    line_pixels = box_width - 2 * offset[0]
    degen_size = line_pixels < 3 * char_width - space
    if line_pixels <= 0:
        return(0)
    
    words = message.split()
    cur_line = 0
    text_lines = [["", 0]]
    for word in words:
        word_length = len(word)
        #Pixels needed for word on current line
        word_size = char_width * word_length - space if text_lines[cur_line][1] == 0 else char_width * word_length + char_width
        #If it can fit
        if word_size + text_lines[cur_line][1] <= line_pixels:
            text_lines[cur_line][0] += ("" if text_lines[cur_line][1] == 0 else " ") + word
            text_lines[cur_line][1] += word_size
            
        #Elif big word or too big for box
        elif word_length > long_word or (char_width * word_length - space) > line_pixels:
            if (line_pixels - text_lines[cur_line][1]) // char_width > 1:
                rem = " "
            else:
                rem = ""
                text_lines.append(["", 0])
                cur_line += 1
            rem += word
            #Overflow logic
            while rem:
                avail = (line_pixels - text_lines[cur_line][1]) // char_width
                if avail <= 0:
                    text_lines.append(["", 0])
                    cur_line += 1
                    
                chunk, rem = rem[:avail], rem[avail:]
                text_lines[cur_line][0] += chunk
                text_lines[cur_line][1] += min(avail * char_width, len(chunk) * char_width)
                
                if rem:
                    if not(degen_size):
                        rem = "-" + rem
                    text_lines.append(["", 0])
                    cur_line += 1
                
        #Create a new line
        else:
            text_lines.append([word, char_width * word_length - space])
            cur_line += 1

    lines = len(text_lines)
    height = (lines * 8 * text_size) + (2 * offset[1]) + (lines - 1) * line_break
    
    if draw:
        if not(box_colour is None):
            buffer.rect(x, y, box_width, height, box_colour, True)
        yt = y + offset[1]
        for line in text_lines:
            if align == "left":
                text(buffer, line[0], x + offset[0], yt, text_size, text_colour)
            elif align == "right":
                text(buffer, line[0], x + offset[0] + line_pixels - line[1], yt, text_size, text_colour)
            elif align == "center":
                text(buffer, line[0], x + offset[0] + (line_pixels - line[1]) // 2, yt, text_size, text_colour)
            else:
                print("Invalid Alignment")
            yt += 8 * text_size + line_break
        
    return(height)